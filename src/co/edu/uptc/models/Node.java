package co.edu.uptc.models;

public class Node {
    private int info;
    private Node next;
    private Node back;
    
    public int getInfo() {
        return info;
    }
    public void setInfo(int info) {
        this.info = info;
    }
    public Node getNext() {
        return next;
    }
    public void setNext(Node next) {
        this.next = next;
    }
    public Node getBack() {
        return back;
    }
    public void setBack(Node back) {
        this.back = back;
    }

    
    
}
